//:- El concepto de protocolo en Swift es similar al de _interface_ en Java.
//:- Un _protocolo_ es una plantilla de métodos, propiedades y otros requisitos que definen una tarea o funcionalidad particular.
//:- No proporciona ninguna implementación, sino que debe ser _adoptada_ por una clase, un _struct_ o una enumeración.
//:- Proporciona un _tipo_ y se puede usar en muchos sitios donde se permiten usar tipos:
//:   - Como el tipo de un parámetro o de un valor devuelto por una función, un método o un inicializador.
//:   - Como el tipo de una constante, variable o propiedad.
//:   - Como el tipo de los ítems de un array, diccionario, o otros contenedores.

